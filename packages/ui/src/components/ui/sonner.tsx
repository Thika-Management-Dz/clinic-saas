"use client"

import {
  CircleCheckIcon,
  InfoIcon,
  Loader2Icon,
  OctagonXIcon,
  TriangleAlertIcon,
} from "lucide-react"
import { useTheme } from "next-themes"
import { Toaster as Sonner, type ToasterProps } from "sonner"

const Toaster = ({ ...props }: ToasterProps) => {
  const { theme = "system" } = useTheme()

  return (
    <Sonner
      // next-themes' useTheme() returns { theme?: string }, so `theme` is
      // `string | undefined` even after the default. Cast to the literal
      // union expected by sonner's ToasterProps. With
      // `exactOptionalPropertyTypes: true` (per @clinic-saas/tsconfig/base),
      // we cannot pass `theme as ToasterProps["theme"]` because
      // `ToasterProps["theme"]` includes `undefined` (optional prop). Cast to
      // the non-optional union instead.
      theme={theme as "system" | "light" | "dark"}
      className="toaster group"
      icons={{
        success: <CircleCheckIcon className="size-4" />,
        info: <InfoIcon className="size-4" />,
        warning: <TriangleAlertIcon className="size-4" />,
        error: <OctagonXIcon className="size-4" />,
        loading: <Loader2Icon className="size-4 animate-spin" />,
      }}
      style={
        {
          "--normal-bg": "var(--popover)",
          "--normal-text": "var(--popover-foreground)",
          "--normal-border": "var(--border)",
          "--border-radius": "var(--radius)",
        } as React.CSSProperties
      }
      {...props}
    />
  )
}

export { Toaster }
